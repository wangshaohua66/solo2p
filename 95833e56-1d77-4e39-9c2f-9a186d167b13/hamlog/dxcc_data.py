def get_continent_name(code: str) -> str:
    """Get full continent name from code."""
    names = {
        "NA": "North America",
        "SA": "South America",
        "EU": "Europe",
        "AF": "Africa",
        "AS": "Asia",
        "OC": "Oceania",
        "AN": "Antarctica",
    }
    return names.get(code, code)
